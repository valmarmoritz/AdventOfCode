        static void Main(string[] args)
        {
            int grid_size_v = 1000;
            int grid_size_h = 1000;

            bool[,] visited = new bool[grid_size_v, grid_size_h];  // vertical, horizontal

            var head = new Location(grid_size_v / 2, grid_size_h / 2);
            var tail = new Location(grid_size_v / 2, grid_size_h / 2);

            visited[tail.v, tail.h] = true;
        
            var Movements = new List<Move>();

            // read movements
            ReadMovements(Movements);

            // walk the grid
            void Walk(string _dir, int _steps, ref bool[,] _visited)
            {
                for (int s = 0; s < _steps; s++)
                {
                    switch (_dir)
                    {
                        case "U":
                            head.v += 1;
                            if (tail.v < head.v - 1)
                            {
                                tail.v += 1;
                                if (tail.h < head.h) tail.h += 1;
                                if (tail.h > head.h) tail.h -= 1;
                            }
                            break;
                        case "D":
                            head.v -= 1;
                            if (tail.v > head.v + 1)
                            {
                                tail.v -= 1;
                                if (tail.h < head.h) tail.h += 1;
                                if (tail.h > head.h) tail.h -= 1;
                            }
                            break;
                        case "R":
                            head.h += 1;
                            if (tail.h < head.h - 1)
                            {
                                tail.h += 1;
                                if (tail.v < head.v) tail.v += 1;
                                if (tail.v > head.v) tail.v -= 1;
                            }
                            break;
                        case "L":
                            head.h -= 1;
                            if (tail.h > head.h + 1)
                            {
                                tail.h -= 1;
                                if (tail.v < head.v) tail.v += 1;
                                if (tail.v > head.v) tail.v -= 1;
                            }
                            break;
                    }
                    _visited[tail.v, tail.h] = true;
                }
            }

            foreach (var m in Movements)
            {
                Walk(m.direction, m.steps, ref visited);
                Console.WriteLine(m.direction + " " + m.steps + "   H: v" + head.v + " h" + head.h + "  T: v" + tail.v + " h" + tail.h);
            }

            int visited_count = 0;
            for (int h = 0; h < grid_size_h; h++)
                for (int v = 0; v < grid_size_v; v++)
                    if (visited[h, v])
                        visited_count += 1;

            Console.WriteLine("Locations visited: " + visited_count);
            Console.WriteLine();
            Console.Write("Press <ENTER> to continue... ");
            Console.ReadLine();

            // ten-knot rope
            bool[,] ten_knot_tail_visited = new bool[grid_size_v, grid_size_h];  // vertical, horizontal
            ten_knot_tail_visited[grid_size_v / 2, grid_size_h / 2] = true;

            List<Location> rope = new List<Location>();

            for (int n = 0; n < 10; n++)
            {
                rope.Add(new Location(grid_size_v / 2, grid_size_h / 2));
            }

            foreach (var m in Movements)
            {
                Walk10KnotRope(m.direction, m.steps, ref rope, ref ten_knot_tail_visited);
                Console.WriteLine(m.direction + " " + m.steps + "   H: v" + rope[0].v + " h" + rope[0].h + "  T: v" + rope[9].v + " h" + rope[9].h);
                //Visualize(rope, grid_size_v/2, grid_size_h/2);
                //Console.ReadLine();
            }

            int ten_knot_tail_visited_count = 0;
            for (int h = 0; h < grid_size_h; h++)
                for (int v = 0; v < grid_size_v; v++)
                    if (ten_knot_tail_visited[h, v])
                        ten_knot_tail_visited_count += 1;

            Console.WriteLine("Locations that the 10-knot rope's tail visited: " + ten_knot_tail_visited_count);

        }

        private static void Visualize(List<Location> rope, int _start_v, int _start_h)
        {
            string top_knot = ".";

            for (int v = 520; v > 480; v--)
            {
                for (int h = 480; h < 520; h++)
                {
                    if (v == _start_v && h == _start_h)
                    {
                        top_knot = "s";
                    }
                    else
                    {
                        top_knot = ".";
                    }

                    for (int k = 9; k >= 0; k--)
                    {
                        if (rope[k].h == h && rope[k].v == v)
                        {
                            switch (k)
                            {
                                case 0: top_knot = "H"; break;
                                case 9: top_knot = "T"; break;
                                default: top_knot = k.ToString(); break;
                            }
                        }
                    }
                    Console.Write(top_knot);
                }
                Console.WriteLine();
            }
            Console.WriteLine();
        }

        private static void Walk10KnotRope(string _dir, int _steps, ref List<Location> _rope, ref bool[,] _ten_knot_tail_visited)
        {
            for (int s = 0; s < _steps; s++)
            {
                for (int k = 0; k < _rope.Count; k++)
                {
                    WalkRopeElement(ref _rope, k);
                }
            _ten_knot_tail_visited[_rope[9].v, _rope[9].h] = true;
            }

            void WalkRopeElement(ref List<Location> _rope, int _knot)
            {
                if (_knot == 0)
                {
                    switch(_dir)
                    {

                        case "U":
                            _rope[_knot].v += 1;
                            break;
                        case "D":
                            _rope[_knot].v -= 1;
                            break;
                        case "R":
                            _rope[_knot].h += 1;
                            break;
                        case "L":
                            _rope[_knot].h -= 1;
                            break;
                    }
                }
                else
                {
                    if (_rope[_knot].v < _rope[_knot - 1].v - 1) 
                    { 
                        _rope[_knot].v += 1; 
                        if (_rope[_knot].h < _rope[_knot -1].h) _rope[_knot].h += 1;
                        if (_rope[_knot].h > _rope[_knot -1].h) _rope[_knot].h -= 1;
                    }

                    else if (_rope[_knot].v > _rope[_knot - 1].v + 1)
                    {
                        _rope[_knot].v -= 1;
                        if (_rope[_knot].h < _rope[_knot - 1].h) _rope[_knot].h += 1;
                        if (_rope[_knot].h > _rope[_knot - 1].h) _rope[_knot].h -= 1;
                    }

                    else if (_rope[_knot].h < _rope[_knot - 1].h - 1)
                    {
                        _rope[_knot].h += 1;
                        if (_rope[_knot].v < _rope[_knot - 1].v) _rope[_knot].v += 1;
                        if (_rope[_knot].v > _rope[_knot - 1].v) _rope[_knot].v -= 1;
                    }

                    else if (_rope[_knot].h > _rope[_knot - 1].h + 1)
                    {
                        _rope[_knot].h -= 1;
                        if (_rope[_knot].v < _rope[_knot - 1].v) _rope[_knot].v += 1;
                        if (_rope[_knot].v > _rope[_knot - 1].v) _rope[_knot].v -= 1;
                    }
                }
            }
        }

